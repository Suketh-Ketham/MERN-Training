const order = {
  orderId: "ORD1001",
  customer: {
    name: "Anita",
    address: {
      city: "Hyderabad",
      pincode: 500085
    }
  },
  items: [
    { product: "Laptop", price: 70000 }
  ]
};

// 1. Create a deep copy
const orderCopy = JSON.parse(JSON.stringify(order));

// 2. Modify copied object
orderCopy.customer.address.city = "Noida";
orderCopy.items[0].price = 45000;

// 3. Verify original remains unchanged
console.log("Original Order:");
console.log(order);

console.log("\nCopied Order:");
console.log(orderCopy);
